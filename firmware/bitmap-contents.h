// Make writing bitmaps a bit more readable.
constexpr uint64_t operator""_bitmap(const char *str, size_t) {
  uint64_t result = 0;
  for (/**/; *str; ++str) {
    result |= (*str != ' ');
    result <<= static_cast<uint64_t>(1);
  }
  return result;
}

